/**
 * Apply safe word-boundary replacements to a string.
 * Skips purely technical strings (math symbols, formulas).
 */
function replaceWords(text) {
  if (!text) return text;
  if (/[→⇌÷×±∞√∫∑πθρΩωΔ²³⁴⁺⁻°₀₁₂₃₄₅₆₇₈₉≈≠≤≥∂⟹°]/.test(text)) return text;
  if (text.includes('$$') || text.includes('\\\\\\\\') || text.includes('&&')) return text;
  if (text.length < 4) return text;

  let result = text;
  const sorted = [...SAFE_REPLACEMENTS].sort((a, b) => b[0].length - a[0].length);

  for (const [search, replace] of sorted) {
    if (!search) continue;
    const escaped = search.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const regex = new RegExp(`\\b${escaped}\\b`, 'gi');
    result = result.replace(regex, replace);
  }

  result = result.replace(/\s+/g, ' ').trim();
  return result;
}

